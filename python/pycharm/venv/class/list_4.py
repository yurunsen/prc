#向列表的末尾添加一个元素
lst=[10,20,30]
lst.append(49)
print(lst,id(lst))
#至少添加一个元素
lst2=[40,50]
lst.extend(lst2)
print(lst,id(lst))
#任意位置插入
lst.insert(1,34)
print(lst,id(lst))
#切除替换
lst3=[35,54]
lst[1:]=lst3
print(lst,id(lst))
#删除
lst.remove(10)
print(lst,id(lst))
#删除指定索引
lst.pop(1)
'''不指定参数删除列表中最后一个'''
print(lst,id(lst))
#切片删除
lst=[10,20,30]
lst_1=lst[1:2]
print(lst_1,id(lst_1))
#或者
lst=[10,20,40]
lst[1:2]=[]
print(lst,id(lst))
#清空列表
lst.clear()
print(lst)
#删除列表
del lst